        <h4 class="text-3xl lg:text-5xl font-bold text-start pt-10 pb-5">Luomalan poliittinen filosofia / The political philosophy of Luomala</h4>


         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Asiat eivät muutu, jos antaa luottamuksen ihmisille jotka eivät ole oikeasti tekemässä muutosta, vaan perustelevat miksi vanhassa vara parempi ja uudessa on vain uhan siemen sekä muutos tehdään sitten joskus, jos milloinkaan. Vanhat johtajat eivät rakenna uutta yhteiskuntaa tai korjaa ratkaisemattomia ongelmia, vaan he sopeuttavat ihmiset hyväksymään olosuhteet, jotka olisi voitu hoitaa eri tavalla ihmisten toimesta, joiden kädet eivät ole sidottu vanhoihin traditioihin." - Mikko Luomala, 12.5.2024</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Tyhjän ja mitätöinnin saa ilmaiseksi ja maailma ei ole kenellekkään mitään velkaa, mutta maailmaa voi yrittää muokata. Mitä maailman pitäisi olla? / Empty and cancelation is free and the world owes no one anything, but you try can shape the world. What should the world be?" - Mikko Luomala</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Me ihmiset olemme se järjestelmä ja järjestelmä on niin tehokas, luetettava, rehellinen, kykenevä, avoin sekä oikeudenmukainen kuin ovat ne ihmiset. / We the people are the system and the system is as efficient, accountable, honest, capable, transparent and fair as are the people." - Mikko Luomala</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Minä olen Pidä Puolesi Suomi/Stand Your Ground Finland ajattelun perustaja" (21.10.2022) - Mikko Luomala</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Päivän päätteeksi meillä on rajoitettu sananvapaus / At the end of the day, we have limited freedom of expression" - Mikko Luomala</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Tämä ei ole helvetti, mutta lähellä sitä / This ain't hell, but it is close" - Mikko Luomala</p>
         <div>&nbsp;</div>
        <p class="font-bold mb-2">"Suomessa tarvitaan etsivää demokraattista toimintaa (etsivä demokratia), jotta äänet jotka ovat vaijenneet tulisivat aidosti kuulluksi päätöksenteossa./ Finland needs outreach democracy/an exploratory democracy to ensure that voices that have been silenced are genuinely heard in decision-making." - Mikko Luomala, 5.12.2023</p>
          <div>&nbsp;</div>
        <p class="font-bold mb-2">"Auttamalla yhteisöä, autat itseäsi. Auta meitä ja autat itseäsi. / By helping the community, you help yourself. Help us and you help yourself." - Mikko Luomala, 5.12.2023</p>
          <div>&nbsp;</div>
        <p class="font-bold mb-2">"Kokonaispolitiikassa katsotaan asioita kokonaisuuksina joka tarkoittaa, että talous, ilmasto, maatalous, maahanmuuttoasiat, puolustus, turvallisuus, kyberturvallisuus, koulutus, terveys, kulttuuri, tutkimus jne on otettava huomioon päätöksenteossa. Meidän on tehtävä pitkäkestoista politiikkaa, joka perustuu todisteaineistopohjaiseen näyttöön. Ihmiset pettyvät kun huomaavat että muutos tapahtuu pistemäisesti ja se ei tule kuin harvoille valituille tai mikään ei muutu. Muutos tapahtuu luottamalla uusimpaan tietoon, eikä pitäytymällä vanhoissa uskomuksissa." - Mikko Luomala, 21.5.2024</p>
